package Personas;

public class Persona {
    String nombre;
    String apellido;
    String telefono;
    String direccion;

    public Persona(String nombre, String apellido, String telefono, String direccion) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.telefono = telefono;
        this.direccion = direccion;
    }   
}
